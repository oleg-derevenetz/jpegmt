QuantizationTable::QuantizationTable()
{
}

QuantizationTable::QuantizationTable(const uint16_t table[Dct::BlockSize2])
{
  memcpy(m_table, table, sizeof(m_table));
}

bool QuantizationTable::isValid() const
{
  return m_table[0] > 0;
}

uint16_t QuantizationTable::maxValue() const
{
  uint16_t maxValue = m_table[0];
  for (int i = 1; i < Dct::BlockSize2; i++)
    maxValue = std::max(maxValue, m_table[i]);
  return maxValue;
}

QuantizationTable QuantizationTable::scaled(int scale, int valueLimit) const
{
  QuantizationTable scaled;

  for (int i = 0; i < Dct::BlockSize2; i++)
  {
    int32_t temp = ((int32_t)m_table[i] * scale + 50) / 100;
    /* limit the values to the valid range */
    if (temp <= 0)
      temp = 1;
    if (temp > 32767)
      temp = 32767; /* max quantizer needed for 12 bits */
    if (temp > valueLimit)
      temp = valueLimit;

    scaled.m_table[i] = (uint16_t)temp;
  }

  return scaled;
}

