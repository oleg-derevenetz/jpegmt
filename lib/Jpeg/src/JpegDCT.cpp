HuffmanTable::HuffmanTable()
{
}

HuffmanTable::HuffmanTable(const uint8_t bits[17], const uint8_t* values, int maxValue)
{
  memcpy(m_bits, bits, sizeof(m_bits));
  memcpy(m_values, values, valuesCount()*sizeof(m_values[0]));
  m_maxValue = maxValue;
}

int HuffmanTable::valuesCount() const
{
  int count = 0;

  for (int i = 1; i <= 16; i++)
    count += m_bits[i];
  return count;
}

